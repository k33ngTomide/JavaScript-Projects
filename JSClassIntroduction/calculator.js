
let left_operator;
let right_operator;
let operator;

left_operator = 14;
operator = "-";
right_operator = 34;


switch(operator) {
  case "+":
    console.log(left_operator + right_operator);
    break;
  case "-":
    console.log(left_operator - right_operator);
    break;
  case "*":
    console.log(left_operator - right_operator);
    break;
  case "/":
    console.log(left_operator - right_operator);
    break;
  case "%":
    console.log(left_operator - right_operator);
    break;
  default:
    console.log(NaN)
}





















